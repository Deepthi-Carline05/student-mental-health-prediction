import streamlit as st
import pandas as pd
from sklearn.preprocessing import LabelEncoder
from sklearn.tree import DecisionTreeClassifier

# -------------------------------
# TITLE
# -------------------------------
st.title("🎓 Student Mental Health Risk Predictor")

# -------------------------------
# LOAD DATA
# -------------------------------
df = pd.read_csv("/content/student_mental_health_.csv")
df.columns = df.columns.str.strip()

st.subheader("📊 Dataset Preview")
st.dataframe(df.head())

# -------------------------------
# DATA PREPROCESSING
# -------------------------------
df = df.drop(columns=["Timestamp"])  # remove unnecessary column

le = LabelEncoder()

for col in df.columns:
    df[col] = le.fit_transform(df[col])

# -------------------------------
# TRAIN MODEL
# -------------------------------
X = df.drop(columns=[df.columns[-1]])  # all except last column
y = df[df.columns[-1]]                # target column

model = DecisionTreeClassifier()
model.fit(X, y)

# -------------------------------
# USER INPUTS
# -------------------------------
st.subheader("🧾 Enter Student Details")

# Reload original dataset for dropdown values
df_original = pd.read_csv("student_mental_health_.csv")
df_original.columns = df_original.columns.str.strip()

age = st.selectbox("Age Group", df_original['Age Group'].unique())
gender = st.selectbox("Gender", df_original['Gender'].unique())
education = st.selectbox("Education Level", df_original['Current Education Level'].unique())
pressure = st.selectbox("Academic Pressure", df_original['How much academic pressure do you feel?'].unique())
stress = st.selectbox("Stress Frequency", df_original['How often do you feel stressed due to studies?'].unique())
sleep = st.selectbox("Sleep Hours", df_original['How many hours do you sleep on average per night?'].unique())

# Encode input
input_data = [age, gender, education, pressure, stress, sleep]

input_encoded = []
for i, col in enumerate(X.columns):
    encoder = LabelEncoder()
    encoder.fit(df_original[col])
    input_encoded.append(encoder.transform([input_data[i]])[0])

# -------------------------------
# PREDICT
# -------------------------------
if st.button("🔍 Predict"):

    prediction = model.predict([input_encoded])[0]

    st.subheader("📌 Prediction Result")

    if prediction == 1:
        st.error("⚠️ High Mental Health Risk")
    else:
        st.success("✅ Low Mental Health Risk")